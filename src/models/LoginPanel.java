    void loginAction() {
        String Username = user.getText().toString();
        String password = pass.getText().toString();

        boolean isUser = dbcon.checkUser(Username, password);

        User user = new User();

        if (isUser == true) {
            user = dbcon.getUser(Username);
            if (user.getType() == "Director") {
                AuthorityPanel adminPanel = new AuthorityPanel(user);
                setVisible(false);
                adminPanel.setVisible(true);
            } else if (user.getType() == "Employee") {
                EmployeePanel workerPanel = new EmployeePanel(user);
                setVisible(false);
                workerPanel.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Incorrect Username or Password! Please try again.");
        }

    }

