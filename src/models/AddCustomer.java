    void validation() {
        if (name.getText().trim().isEmpty() || phone.getText().trim().isEmpty() || address.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill up all field ");
        } 
        else if(db.checkCustomer(name.getText().toString())== true){
            JOptionPane.showMessageDialog(this, "Customer name is availabe. Please select another name.");
        }
        else if(validation.checkInteger(phone.getText().toString())== false){
            JOptionPane.showMessageDialog(this, "Please input a valid phone number");
        }
        else {
            addAction();
        }
    }

    void addAction() {
        String Name = name.getText().toString();
        String Phone = phone.getText().toString();
        String Address = address.getText().toString();
        int AddedBy = user.getId();
        String Company = company.getText().toString();

        String sql = "insert into customer(name,phone,address,added_by,company,date) values ('" + Name + "','" + Phone + "','" + Address + "'," + AddedBy + ",'" + Company + "',now())";
        db.InsertUpdateRowInTable(sql);
        JOptionPane.showMessageDialog(this, "Customer  added succesfully");
    }

